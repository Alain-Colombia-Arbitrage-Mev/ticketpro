interface LanguageState {
  language: Language;
  setLanguage: (language: Language) => void;
}

// Funci√≥n para obtener el idioma inicial (siempre espa√±ol por defecto)
const getInitialLanguage = (): Language => {
  if (typeof window === 'undefined') return 'es';
  
  // FORZAR ESPA√ëOL POR DEFECTO - Descomentar la siguiente l√≠nea si quieres resetear siempre a espa√±ol
  // if (typeof window !== 'undefined') localStorage.removeItem('language-storage');
  
  try {
    const stored = localStorage.getItem('language-storage');
    if (stored) {
      const parsed = JSON.parse(stored);
      const savedLang = parsed.state?.language;
      console.log('Language loaded from storage:', savedLang);
      return savedLang || 'es';
    }
  } catch (error) {
    console.warn('Error loading language from storage:', error);
  }
  
  console.log('No language in storage, defaulting to: es');
  return 'es'; // Siempre espa√±ol por defecto
};

export const useLanguageStore = create<LanguageState>()(
  persist(
    (set) => ({
      language: getInitialLanguage(),
      
      setLanguage: (language) => {
        console.log('üåê Idioma cambiado a:', language);
        set({ language });
      },
    }),
    {
      name: 'language-storage',
      // Verificar que localStorage est√© disponible (SSR safe)
      storage: typeof window !== 'undefined' ? localStorage : undefined,
    }
  )
);

